const PATTERNS = {
  solid: { name: 'Solid', stripeIndices: [] },
  tabby: { name: 'Tabby', stripeIndices: [1, 3] },
  spotted: { name: 'Spotted', stripeIndices: [0, 2] },
  tuxedo: { name: 'Tuxedo', stripeIndices: [] },
};

class CatRenderer {
  constructor(canvas) {
    this.canvas = canvas;
    this.ctx = canvas.getContext('2d');
    this.pixelSize = 4;
    this.palette = PALETTES.orange;
    this.pattern = 'solid';
    this.currentSprite = 'idle_0';
    this.flipX = false;
    this.canvas.width = 16 * this.pixelSize;
    this.canvas.height = 16 * this.pixelSize;
    this.canvas.style.imageRendering = 'pixelated';
  }

  setPalette(name) {
    if (PALETTES[name]) {
      this.palette = PALETTES[name];
      this.render();
    }
  }

  setPattern(name) {
    this.pattern = name;
    this.render();
  }

  setSprite(name) {
    if (SPRITES[name]) {
      this.currentSprite = name;
      this.render();
    }
  }

  setFlip(flipped) {
    this.flipX = flipped;
    this.render();
  }

  getColor(index) {
    const p = this.palette;
    switch(index) {
      case 0: return null; // transparent
      case 1: return p.body;
      case 2: return p.belly;
      case 3: return p.outline;
      case 4: return p.eye;
      case 5: return p.highlight;
      case 6: return p.nose;
      case 7: return '#ffffaa'; // sparkle/steam
      default: return null;
    }
  }

  applyPattern(grid) {
    if (this.pattern === 'solid') return grid;
    const p = this.palette;
    const result = grid.map(row => [...row]);

    if (this.pattern === 'tabby') {
      // Add stripe rows on body pixels
      for (let r = 0; r < 16; r++) {
        for (let c = 0; c < 16; c++) {
          if (result[r][c] === 1 && (r % 3 === 0)) {
            result[r][c] = 8; // stripe color
          }
        }
      }
    } else if (this.pattern === 'spotted') {
      // Add spots
      const spots = [[3,3],[5,7],[8,4],[7,10],[4,12]];
      for (const [sr, sc] of spots) {
        for (let dr = -1; dr <= 1; dr++) {
          for (let dc = -1; dc <= 1; dc++) {
            const nr = sr + dr, nc = sc + dc;
            if (nr >= 0 && nr < 16 && nc >= 0 && nc < 16 && result[nr][nc] === 1) {
              result[nr][nc] = 8;
            }
          }
        }
      }
    } else if (this.pattern === 'tuxedo') {
      // White belly + black body
      for (let r = 4; r < 10; r++) {
        for (let c = 4; c < 12; c++) {
          if (result[r][c] === 2) result[r][c] = 2; // keep belly white
        }
      }
    }
    return result;
  }

  render() {
    const ctx = this.ctx;
    const ps = this.pixelSize;
    const grid = SPRITES[this.currentSprite];
    if (!grid) return;

    ctx.clearRect(0, 0, this.canvas.width, this.canvas.height);

    if (this.flipX) {
      ctx.save();
      ctx.translate(this.canvas.width, 0);
      ctx.scale(-1, 1);
    }

    const patterned = this.applyPattern(grid);
    const stripeColor = this.palette.stripe;

    for (let r = 0; r < 16; r++) {
      for (let c = 0; c < 16; c++) {
        const idx = patterned[r][c];
        let color = this.getColor(idx);
        if (idx === 8) color = stripeColor;
        if (color) {
          ctx.fillStyle = color;
          ctx.fillRect(c * ps, r * ps, ps, ps);
        }
      }
    }

    if (this.flipX) ctx.restore();
  }
}

